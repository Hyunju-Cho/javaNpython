def ex01():
    dc={'새우깡':700,'콘치즈':850,'꼬깔콘':750}
    dc['홈런볼']=900
    print(dc)

def ex02():
    dc={'새우깡':700,'콘치즈':850,'꼬깔콘':750,'홈런볼':900}
    for i in dc:
        dc[i]+=100
    print(dc)

def ex03():
    dc={'새우깡':700,'콘치즈':850,'꼬깔콘':750,'홈런볼':900}
    print(dc)
    del dc['콘치즈']
    dc['치즈콘']=950
    print(dc)


ex01()
ex02()
ex03()